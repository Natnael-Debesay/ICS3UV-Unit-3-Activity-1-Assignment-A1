// initialize numbers
const RADIUS: number = 5.6;
const PI: number = 3.14;

// INPUT - none

// PROCESS
// calculate the area

let answer: number = RADIUS * RADIUS * PI;

// OUTPUT
// display the result 
console.log(
  "The area of a circle with a radius of " + RADIUS + " is: " + answer
);

console.log("\nDone.");
